<div class="abt-title">
<h3 style="text-align: center;">UK GCE, AS/A2, International A-levels</h3>
<h3>What is the difference between GCE A-level and International A-level?</h3>
</div>
<div class="about-desc">
<p>he GCE A-level is a linear qualification taken over two years by students at school in the
UK. International students can still take it but they should note they will take regional
versions and will sit papers at slightly different times. The examinations take place in
June. GCE A-levels carry UCAS tariff points.</p>

<p>The International A-level is especially for students studying outside of the UK. It follows
a modular structure so you can build the qualification over time. Examinations take
place in January, June and October. International A-levels can be used to access
university courses.</p>
</div>



<br><br>
<div class="abt-title">
<h3>Do I have to an IGCSE in a subject before taking the GCE/International A-
level in that subject?</h3>
</div>
<div class="about-desc">
<p>No. However IGCSEs do provide a good foundation. You may wish to study
IGCSE material informally before moving on to GCE/International A-level.</p>

</div>




<br><br>
<div class="abt-title">
<h3>What do A-level, AS and A2 mean?</h3>
</div>
<div class="about-desc">
<p>In GCE A-level one examination takes place at the end of the course. There
are GCE AS-levels which count as &quot;half&quot; an A-level. University places are
awarded on the basis of grades at A-level but AS awards also attract UCAS
points and are often counted towards university entrance.</p>

<p>A2 refers to the final stage of International A-levels. Students who have
successfully completed AS units in a subject move on to do A2 units, which
are of a higher standard. Completing the correct sequence of units at both
AS and A2 level means you have finished a full International A-level in a
subject. University entrance is at the discretion of the university based on
your AS and A2 grades.</p>
