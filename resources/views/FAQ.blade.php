<h3>What Is IB?</h3>
</div>
<div class="about-desc">
<p>The International Baccalaureate (IB) Diploma Programme is an
internationally recognised qualification for students aged 16 to 19. It is
based around detailed academic study of a wide range of subjects,
including languages, the arts, science, maths, history and geography.</p>


<p>It leads to a single qualification, rather than separate qualifications
for individual subjects. However if you don&#39;t achieve the full diploma,
you&#39;ll be awarded a certificate for each subject taken.</p>

<p>1. learn how to learn.</p>
<p>2. ask challenging questions.</p>
<p>3. develop a strong sense of your own identity and culture.</p>
<p>4. develop the ability to communicate with and understand
people from other countries and cultures.</p>

<p>The IB Diploma Programme is at level 3 on the UK National
Qualifications</p>

Framework equivalent to A-levels and BTEC Nationals which also gain
you entry to university.
</div>




<br><br>
<div class="abt-title">
<h3>What is the Course structure?</h3>
</div>
<div class="about-desc">
<p>The IB Diploma Programme is made up of a compulsory &#39;core&#39;, plus
six separate subjects where you have some choice over what you
study.</p>
</div>



<br><br>
<div class="abt-title">
<h3>Compulsory core</h3>

</div>
<div class="about-desc">
	<p>The compulsory core contains three elements:</p>
<ul>
	<li>theory of knowledge: you&#39;ll learn about the bases of knowledge,
and how to analyse evidence and express yourself in rational
argument; you&#39;ll also be encouraged to draw on experiences
you&#39;ve gained outside the classroom</li>
<li>creativity, action and service: this part of the programme
encourages you to get involved in theatre or music activities,
sports and/or community service</li>
<li>extended essay: you&#39;ll investigate a particular topic of interest
and be asked to write a 4,000 word essay about it</li>
</ul>
</div>


<br><br>
<div class="abt-title">
<h3>Optional subjects</h3>
</div>
<div class="about-desc">
	<p>As well as the three core elements, you&#39;ll also select one subject from
each of the following six areas:</p>
<ul>
<li>first language (normally your mother tongue).</li>
<li>second language (this could be a language you already study,
or a new one).</li>
<li>experimental sciences (biology, chemistry, physics, design
technology).</li>
<li>mathematics and computer science.</li>
<li>the arts (visual, music and theatre).</li>
<li>individuals and society (history, psychology, geography). </li>
</ul>
<p>Normally, you&#39;ll study three of your six optional subjects at a &#39; higher&#39;
level (240 teaching hours per subject), and the other three at a
&#39;standard&#39; level (150 teaching hours). However, you can also opt to
take four subjects at the higher level and two at the standard level.</p>


<br>
<p><b>How you are assessed</b></p>
<p>Most of the assessment is done through exams, marked externally.
However, in nearly all subjects, some of the assessment is carried out
by your teachers, who mark individual pieces of coursework. – the IAs
or Internal Assessments.</p>
<p>The Diploma normally takes two years to complete, with exams
taking place in May and November.</p>
</div>




