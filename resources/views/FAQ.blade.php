<div class="about-desc">
<p>You are awarded points for each part of the programme, up to a
maximum of 45:</p>

<p>up to seven points for each of the six optional subjects you take</p>
<p>up to three points from your performance in the core elemen</p>
<p>To achieve a full diploma, you must score 24 points or more.</p>

